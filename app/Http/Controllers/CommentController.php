    {
        $validated = $request->validate([
            'body' => ['required', 'string'],
            'user_id' => ['required'],
            'commentable_id' => ['integer'],
            'commentable_type' => ['string'],
        ]);
        $validated['user_id'] = (int)$validated['user_id'][0];

        Comment::create($validated);
        return redirect(request()->headers->get('referer'));
    }

